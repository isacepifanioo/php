<?php 

    // e uma forma podemos simplificar 
    // operadores: +=, -=, /=, *= %=

    $a = 0;
    echo $a += 10; // isso e a mesma coisa de $a = $a + 10 = 10
    echo '<br>';

    $b = 0; 
    echo $b -= 5; // $b = $b - 5 = -5
    echo '<br>';

    $a = 10;
    echo $a /= 2; // isso e a mesma coisa de $a = $a / 2 = 5
    echo '<br>';

    $a = 10;
    echo $a %= 3; // isso e a mesma coisa de $a = $a % 3 = 1
    echo '<br>';
